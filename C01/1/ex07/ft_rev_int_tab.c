void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int middle;
	int tmp;

	i = 0;
	middle = size / 2;
	while (i < middle)
	{
		tmp = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = tmp;
		i++;
	}
}
